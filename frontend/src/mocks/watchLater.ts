/** 이 일수를 넘기면 ‘오래됨’ 배지 (부담 완화용 muted 톤) */
export const WATCH_LATER_STALE_DAYS = 21

export function isWatchLaterStale(addedAt: string): boolean {
  const ms = Date.now() - new Date(addedAt).getTime()
  return ms > WATCH_LATER_STALE_DAYS * 86400 * 1000
}

function daysAgoISO(days: number): string {
  const d = new Date()
  d.setDate(d.getDate() - days)
  return d.toISOString()
}

